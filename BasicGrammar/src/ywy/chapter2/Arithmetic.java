package ywy.chapter2;

public class Arithmetic {
	
	public static final int additive(int left, int right) {
		return left + right;
	}
	
	public static final int subtraction(int left, int right) {
		return left - right;
	}
	
	public static final int multiplication(int left, int right) {
		return left * right;
	}
	
	public static int division(int left, int right) {
		return left / right;
	}
	
	public static final int remainder(int left, int right) {
		return left % right;
	}
}
